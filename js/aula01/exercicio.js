
let nome = prompt("Qual seu nome?");
let nota01 = Number(prompt(` Qual a sua primeira nota?`));
let nota02 = Number(prompt("Qual a sua segunda nota?"));

let = media = (nota01 * (6 / 10)) + (nota02 * (4 / 10));;

if (media <= 7) {

    alert(`Olá aluno ${nome} sinto lhe informar mas a sua média ${media} não foi alta o suficiente para ser aprovado no seu curso`);

}
else {
    alert(`Olá aluno ${nome} é com grande exito lhe informar sua ${media} alta o suficiente para ser aprovado no seu curso`);
}
